    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
         if(txtGtA.getText().trim().isEmpty() || txtGtB.getText().trim().isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ 2 giá trị");
        }
        else
        {
            boolean hopLe = true;
           String message = "";
           
            try {
                number.setGtA(Integer.parseInt(txtGtA.getText().trim()));
            } catch (NumberFormatException e) {
                hopLe=false;
                message+="Gía trị A không hợp lệ\n";
            }
            
            try {
                number.setGtB(Integer.parseInt(txtGtB.getText().trim()));
            } catch (NumberFormatException e) {
                hopLe=false;
                message+="Gía trị B không hợp lệ\n";
            }
            
            if(hopLe)
            {
                try {
                    File f = new File("number.dat");
                    FileWriter fw = new FileWriter(f);
                    BufferedWriter bw = new BufferedWriter(fw);
                    
                    bw.write(number.toString());
                    bw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(this, "Lưu file thành công");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Lưu file không thành công");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, message);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

