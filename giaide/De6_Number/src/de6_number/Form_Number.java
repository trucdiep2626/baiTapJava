    private void btnCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCongActionPerformed
        if(txtGtA.getText().trim().isEmpty() || txtGtB.getText().trim().isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ 2 giá trị");
        }
        else
        {
            boolean hopLe = true;
           String message = "";
           
            try {
                number.setGtA(Integer.parseInt(txtGtA.getText().trim()));
            } catch (NumberFormatException e) {
                hopLe=false;
                message+="Gía trị A không hợp lệ\n";
            }
            
            try {
                number.setGtB(Integer.parseInt(txtGtB.getText().trim()));
            } catch (NumberFormatException e) {
                hopLe=false;
                message+="Gía trị B không hợp lệ\n";
            }
            
            if(hopLe)
            {
                lbKq.setText(Integer.toString(number.cong()));
            }
            else
            {
                JOptionPane.showMessageDialog(this, message);
            }
        }
    }//GEN-LAST:event_btnCongActionPerformed

