    private void btnDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileActionPerformed
        try{
            FileInputStream fis=new FileInputStream("listBook.dat");
            InputStreamReader isr=new InputStreamReader(fis);
            BufferedReader br=new BufferedReader(isr);
            String line=br.readLine();
           
            while (line!=null)
            {
                String [] arr=line.split(",");
                String id=arr[0];
               
                String au=arr[1];
                
                String ca=arr[2];
                String tit=arr[3];
                Book s = new Book(id, au, ca, tit);
                listBook.add(s);
              
                line=br.readLine();//đọc dòng tiếp theo
            }
             
        }catch(Exception e){
            e.printStackTrace();
            System.out.println("Doc file that bai");
        }
           model = (DefaultTableModel) tbThongTin.getModel();
        for(Book b : listBook)
        {
            model.addRow(b.toStringArray());
        }
    }//GEN-LAST:event_btnDocFileActionPerformed

