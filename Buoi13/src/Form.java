    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
      if(txtTittle.getText().compareTo("")==0 || txtID.getText().compareTo("")==0 ||txtAuthor.getText().compareTo("")==0 || cbCategory.getSelectedItem()==null)
        {
          JOptionPane.showMessageDialog(null, "Thông tin chưa nhập đủ");
      }
      else
      {
          String ID=txtID.getText();
       String Author=txtAuthor.getText();
       String title=txtTittle.getText();
       String category=cbCategory.getSelectedItem().toString();
       Book b = new Book(ID, Author, category, title);
       dss.add(b);
        try{
            File f = new File("listBook.dat");
            FileWriter fw= new FileWriter(f);
            BufferedWriter bw= new BufferedWriter(fw);
            for (int i = 0; i < dss.size(); i++) {
                bw.write(dss.get(i).toString());
            }
           
            bw.close();
            fw.close();
        }catch(IOException o){
            
        }
        
      }
    }//GEN-LAST:event_btnLuuActionPerformed

